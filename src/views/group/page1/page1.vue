      习近平欢迎各国使节来华履新，请他们转达对各有关国家领导人和人民的诚挚问候和美好祝愿，表示中国政府将为各国使节履职提供便利和支持，希望使节们发挥桥梁和纽带作用，为增进中国同各国友谊、推动双边关系发展作出积极贡献。习近平指出，中国高度重视发展同各国友好关系，愿进一步巩固传统友谊，增进政治互信，深化务实合作和人文交流，加强在国际事务中的合作。

      使节们再次转达了各自国家领导人对习近平当选连任中国国家主席的诚挚祝贺和对习近平的亲切问候。各国使节热烈祝贺中国全国“两会”胜利召开，高度评价中国改革开放和发展成就，相信中国在实现自身发展的同时，将为促进世界共同发展进步作出更大贡献。使节们表示，各国高度重视发展对华关系。他们对出使中国深感荣幸，将致力于促进各自国家同中国的友好关系，实现互利共赢。

